# Cap. 1 - A jornada do programa
# A função print() mostra texto na tela. É a forma mais simples de "saída".
print("Olá, Gigaverse!")

# O Python também funciona como uma calculadora. Repare na precedência:
# a multiplicação acontece ANTES da soma, como na matemática.
print("2 + 3 * 4   =", 2 + 3 * 4)
print("(2 + 3) * 4 =", (2 + 3) * 4)

# Cada valor tem um TIPO. A função type() revela qual é.
print("O tipo de 42   ->", type(42))
print("O tipo de 3.14 ->", type(3.14))
print("O tipo de 'oi' ->", type("oi"))
